// Classe que representa uma cadeira
public class Cadeira {
    
    // Propriedades (atributos)
    String cor;
    int pernas;
    boolean temEncosto;

    // Método para sentar
    public void sentar() {
        System.out.println("Você está sentado na cadeira.");
    }

    // Método para mover
    public void mover() {
        System.out.println("Você moveu a cadeira.");
    }
}
